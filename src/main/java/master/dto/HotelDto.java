package master.dto;

public class HotelDto {
	String hid;
	String hname;
	String tplace;
	public String getHid() {
		return hid;
	}
	public void setHid(String hid) {
		this.hid = hid;
	}
	public String getHname() {
		return hname;
	}
	public void setHname(String hname) {
		this.hname = hname;
	}
	public String getTplace() {
		return tplace;
	}
	public void setTplace(String tplace) {
		this.tplace = tplace;
	}
}
